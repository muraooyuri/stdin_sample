# それぞれのメソッドによる標準出力
int = 1000
str = "文字列です"
arr = ["a","b","c","d","e"]
yfo = "終わります"

print "pメソッドの出力\n"
print "-----------------\n"
p int
p str
p arr
p yfo
print "\n"

print "printメソッドの出力\n"
print "-----------------\n"
print int
print str
print arr
print yfo
print "\n"
print "\n"

print"putsメソッドの出力\n"
print "-----------------\n"
puts int
puts str
puts arr
puts yfo